package com.example.missao.dto.requisicao;

public class RequisicaoSecao {
    private Long numeroSecao;
    private String tipoDaPesquisa;

    public Long getNumeroSecao() {
        return numeroSecao;
    }

    public void setNumeroSecao(Long numeroSecao) {
        this.numeroSecao = numeroSecao;
    }

    public String getTipoDaPesquisa() {
        return tipoDaPesquisa;
    }

    public void setTipoDaPesquisa(String tipoDaPesquisa) {
        this.tipoDaPesquisa = tipoDaPesquisa;
    }
}
